/** Normalise un prénom : retire les accents, minuscules */
function normalizeName(s: string): string {
  return s.trim().normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase();
}

/**
 * Retourne la fête du prénom pour un prénom donné.
 * Gère les accents (José → jose) et les prénoms composés (Marie-Claire → cherche "Marie" puis "Claire").
 * @returns "MM-DD" ou null si non trouvé
 */
export function getNameDayForName(firstName: string): string | null {
  const normalized = normalizeName(firstName);
  for (const [date, names] of Object.entries(FRENCH_NAMEDAYS)) {
    if (names.some((n) => normalizeName(n) === normalized)) {
      return date;
    }
  }
  // Pas trouvé directement → essaie le premier segment d'un prénom composé
  if (firstName.includes('-')) {
    const firstPart = firstName.split('-')[0];
    const normalizedPart = normalizeName(firstPart);
    for (const [date, names] of Object.entries(FRENCH_NAMEDAYS)) {
      if (names.some((n) => normalizeName(n) === normalizedPart)) {
        return date;
      }
    }
  }
  return null;
}

export interface CompoundNameDay {
  part: string;   // ex : "Marie"
  mmdd: string;   // ex : "01-01"
  label: string;  // ex : "1er janvier"
}

/**
 * Pour les prénoms composés (tiret), retourne les fêtes trouvées
 * pour chaque partie du prénom.
 * Retourne un tableau vide si ce n'est pas un prénom composé ou si aucune fête.
 */
export function getCompoundNameDays(firstName: string): CompoundNameDay[] {
  if (!firstName.includes('-')) return [];
  const parts = firstName.split('-').map((p) => p.trim()).filter(Boolean);
  const results: CompoundNameDay[] = [];
  for (const part of parts) {
    const normalized = normalizeName(part);
    for (const [mmdd, names] of Object.entries(FRENCH_NAMEDAYS)) {
      if (names.some((n) => normalizeName(n) === normalized)) {
        const [m, d] = mmdd.split('-').map(Number);
        const date = new Date(2000, m - 1, d);
        const label = date.toLocaleDateString('fr-FR', { day: 'numeric', month: 'long' });
        results.push({ part, mmdd, label });
        break;
      }
    }
  }
  return results;
}

/**
 * Retourne les prénoms fêtés à une date donnée.
 * @param mmdd format "MM-DD"
 */
export function getNamesForDate(mmdd: string): string[] {
  return FRENCH_NAMEDAYS[mmdd] ?? [];
}
